#!/usr/bin/env python3
"""
Basic Notations Concat
"""


def concat(str1: str, str2: str) -> str:
    """Function that takes a two strings and returns a concatenated string

    Args:
        str1 (str): First String
        str2 (str): Second String

    Returns:
        str: Concatenated String
    """
    return str1 + str2
